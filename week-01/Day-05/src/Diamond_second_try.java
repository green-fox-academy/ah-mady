public class Diamond_second_try {
    public static void main(String[] args) {
        int start = 5;

        for (int i = 0; i <= start ; i++) {
            System.out.print("*");
            for (int j = 0; j < i; j++) {
                System.out.print("*");

            }
            System.out.println();
        }
    }
}
