public class TwoNumbers {
    public static void main(String[] args) {

        int num1 = 22;
        int num2 = 13;

        System.out.println(num2 + num1);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println( (double) (num1 / num2) );
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);

    }
}
