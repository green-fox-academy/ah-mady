public class OneTWoAlot {
    public static void main(String[] args) {

        int num = 3;
        if (num <= 0 ){
            System.out.println("Not Enough");
        }else if (num ==1){
            System.out.println("One");
        }else if (num == 2){
            System.out.println("Two");
        }else{
            System.out.println("A lot");
        }
    }
}
